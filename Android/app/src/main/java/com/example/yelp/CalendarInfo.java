package com.example.yelp;

public class CalendarInfo {

    String bookid, booked_name, booked_email, booked_date, booked_time;

    public String getBookid() {
        return bookid;
    }

    public void setBookid(String bookid) {
        this.bookid = bookid;
    }

    public String getBooked_name() {
        return booked_name;
    }

    public void setBooked_name(String booked_name) {
        this.booked_name = booked_name;
    }

    public String getBooked_email() {
        return booked_email;
    }

    public void setBooked_email(String booked_email) {
        this.booked_email = booked_email;
    }

    public String getBooked_date() {
        return booked_date;
    }

    public void setBooked_date(String booked_date) {
        this.booked_date = booked_date;
    }

    public String getBooked_time() {
        return booked_time;
    }

    public void setBooked_time(String booked_time) {
        this.booked_time = booked_time;
    }
}
